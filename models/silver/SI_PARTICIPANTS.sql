-- SI_PARTICIPANTS: Cleaned and standardized meeting participants with MM/DD/YYYY HH:MM format handling
-- Enhanced transformation with timestamp format validation for MM/DD/YYYY HH:MM format issues

WITH source_data AS (
    SELECT 
        PARTICIPANT_ID,
        MEETING_ID,
        USER_ID,
        JOIN_TIME,
        LEAVE_TIME,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM
    FROM BRONZE.BZ_PARTICIPANTS
    WHERE PARTICIPANT_ID IS NOT NULL  -- Exclude records with null PARTICIPANT_ID
),

timestamp_format_validation AS (
    SELECT 
        *,
        -- Enhanced MM/DD/YYYY HH:MM format validation and conversion for JOIN_TIME
        COALESCE(
            TRY_TO_TIMESTAMP(JOIN_TIME::STRING, 'YYYY-MM-DD HH24:MI:SS'),
            TRY_TO_TIMESTAMP(JOIN_TIME::STRING, 'DD/MM/YYYY HH24:MI'),
            TRY_TO_TIMESTAMP(JOIN_TIME::STRING, 'MM/DD/YYYY HH24:MI'),
            TRY_TO_TIMESTAMP(REPLACE(JOIN_TIME::STRING, ' EST', ''), 'YYYY-MM-DD HH24:MI:SS'),
            JOIN_TIME
        ) AS STANDARDIZED_JOIN_TIME,
        
        -- Enhanced MM/DD/YYYY HH:MM format validation and conversion for LEAVE_TIME
        COALESCE(
            TRY_TO_TIMESTAMP(LEAVE_TIME::STRING, 'YYYY-MM-DD HH24:MI:SS'),
            TRY_TO_TIMESTAMP(LEAVE_TIME::STRING, 'DD/MM/YYYY HH24:MI'),
            TRY_TO_TIMESTAMP(LEAVE_TIME::STRING, 'MM/DD/YYYY HH24:MI'),
            TRY_TO_TIMESTAMP(REPLACE(LEAVE_TIME::STRING, ' EST', ''), 'YYYY-MM-DD HH24:MI:SS'),
            LEAVE_TIME
        ) AS STANDARDIZED_LEAVE_TIME
    FROM source_data
),

data_quality_validation AS (
    SELECT 
        *,
        -- Data Quality Score Calculation
        CASE 
            WHEN PARTICIPANT_ID IS NOT NULL 
                AND MEETING_ID IS NOT NULL 
                AND USER_ID IS NOT NULL 
                AND STANDARDIZED_JOIN_TIME IS NOT NULL 
                AND STANDARDIZED_LEAVE_TIME IS NOT NULL 
                AND STANDARDIZED_LEAVE_TIME > STANDARDIZED_JOIN_TIME
            THEN 100
            WHEN PARTICIPANT_ID IS NOT NULL AND MEETING_ID IS NOT NULL AND USER_ID IS NOT NULL 
            THEN 75
            WHEN PARTICIPANT_ID IS NOT NULL 
            THEN 50
            ELSE 25
        END AS DATA_QUALITY_SCORE,
        
        -- Validation Status
        CASE 
            WHEN PARTICIPANT_ID IS NOT NULL 
                AND MEETING_ID IS NOT NULL 
                AND USER_ID IS NOT NULL 
                AND STANDARDIZED_JOIN_TIME IS NOT NULL 
                AND STANDARDIZED_LEAVE_TIME IS NOT NULL 
                AND STANDARDIZED_LEAVE_TIME > STANDARDIZED_JOIN_TIME
            THEN 'PASSED'
            WHEN PARTICIPANT_ID IS NOT NULL AND MEETING_ID IS NOT NULL 
            THEN 'WARNING'
            ELSE 'FAILED'
        END AS VALIDATION_STATUS
    FROM timestamp_format_validation
),

deduplication AS (
    SELECT *,
        ROW_NUMBER() OVER (PARTITION BY PARTICIPANT_ID ORDER BY UPDATE_TIMESTAMP DESC NULLS LAST, LOAD_TIMESTAMP DESC) as rn
    FROM data_quality_validation
),

final_transformation AS (
    SELECT 
        PARTICIPANT_ID,
        MEETING_ID,
        USER_ID,
        STANDARDIZED_JOIN_TIME AS JOIN_TIME,
        STANDARDIZED_LEAVE_TIME AS LEAVE_TIME,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM,
        DATE(LOAD_TIMESTAMP) AS LOAD_DATE,
        DATE(UPDATE_TIMESTAMP) AS UPDATE_DATE,
        DATA_QUALITY_SCORE,
        VALIDATION_STATUS
    FROM deduplication
    WHERE rn = 1  -- Keep only the latest record per PARTICIPANT_ID
        AND VALIDATION_STATUS != 'FAILED'  -- Exclude failed records from Silver layer
        AND STANDARDIZED_JOIN_TIME IS NOT NULL  -- Ensure timestamp conversion succeeded
)

SELECT * FROM final_transformation
