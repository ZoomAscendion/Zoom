-- Silver Layer Audit Log Table
-- This table must be created first to support audit logging for all other models

SELECT 
    CAST(NULL AS VARCHAR(255)) AS TABLE_NAME,
    CAST(NULL AS VARCHAR(255)) AS COLUMN_NAME,
    CAST(NULL AS VARCHAR(255)) AS ERROR_TYPE,
    CAST(NULL AS VARCHAR(1000)) AS ERROR_DESCRIPTION,
    CAST(NULL AS VARCHAR(255)) AS RECORD_ID,
    CAST(NULL AS VARCHAR(1000)) AS ORIGINAL_VALUE,
    CAST(NULL AS TIMESTAMP_NTZ(9)) AS AUDIT_TIMESTAMP,
    CAST(NULL AS VARCHAR(255)) AS PROCESS_STATUS,
    CAST(NULL AS TIMESTAMP_NTZ(9)) AS PROCESS_START_TIME,
    CAST(NULL AS TIMESTAMP_NTZ(9)) AS PROCESS_END_TIME,
    CAST(NULL AS NUMBER(38,0)) AS RECORDS_PROCESSED,
    CAST(NULL AS NUMBER(38,0)) AS RECORDS_SUCCESS,
    CAST(NULL AS NUMBER(38,0)) AS RECORDS_FAILED
WHERE FALSE
