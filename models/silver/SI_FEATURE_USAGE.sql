{{ config(
    materialized='table',
    unique_key='USAGE_ID'
) }}

WITH bronze_feature_usage AS (
    SELECT 
        USAGE_ID,
        MEETING_ID,
        FEATURE_NAME,
        USAGE_COUNT,
        USAGE_DATE,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM
    FROM {{ source('bronze', 'BZ_FEATURE_USAGE') }}
    WHERE USAGE_ID IS NOT NULL
),

cleaned_feature_usage AS (
    SELECT 
        USAGE_ID,
        MEETING_ID,
        UPPER(TRIM(FEATURE_NAME)) AS FEATURE_NAME,
        COALESCE(USAGE_COUNT, 0) AS USAGE_COUNT,
        COALESCE(
            TRY_TO_DATE(USAGE_DATE::STRING, 'YYYY-MM-DD'),
            TRY_TO_DATE(USAGE_DATE::STRING, 'DD/MM/YYYY'),
            TRY_TO_DATE(USAGE_DATE::STRING, 'MM/DD/YYYY'),
            TRY_TO_DATE(USAGE_DATE::STRING)
        ) AS CLEANED_USAGE_DATE,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM
    FROM bronze_feature_usage
),

validated_feature_usage AS (
    SELECT 
        USAGE_ID,
        MEETING_ID,
        FEATURE_NAME,
        USAGE_COUNT,
        COALESCE(CLEANED_USAGE_DATE, CURRENT_DATE()) AS USAGE_DATE,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM,
        DATE(LOAD_TIMESTAMP) AS LOAD_DATE,
        DATE(UPDATE_TIMESTAMP) AS UPDATE_DATE,
        CASE 
            WHEN FEATURE_NAME IS NOT NULL AND USAGE_COUNT >= 0 AND CLEANED_USAGE_DATE IS NOT NULL THEN 100
            WHEN FEATURE_NAME IS NOT NULL AND USAGE_COUNT >= 0 THEN 85
            WHEN FEATURE_NAME IS NOT NULL THEN 70
            ELSE 50
        END AS DATA_QUALITY_SCORE,
        CASE 
            WHEN FEATURE_NAME IS NOT NULL AND USAGE_COUNT >= 0 AND CLEANED_USAGE_DATE IS NOT NULL THEN 'PASSED'
            WHEN FEATURE_NAME IS NOT NULL AND USAGE_COUNT >= 0 THEN 'WARNING'
            ELSE 'FAILED'
        END AS VALIDATION_STATUS
    FROM cleaned_feature_usage
),

deduped_feature_usage AS (
    SELECT 
        USAGE_ID,
        MEETING_ID,
        FEATURE_NAME,
        USAGE_COUNT,
        USAGE_DATE,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM,
        LOAD_DATE,
        UPDATE_DATE,
        DATA_QUALITY_SCORE,
        VALIDATION_STATUS,
        CURRENT_TIMESTAMP() AS CREATED_AT,
        CURRENT_TIMESTAMP() AS UPDATED_AT,
        ROW_NUMBER() OVER (PARTITION BY USAGE_ID ORDER BY UPDATE_TIMESTAMP DESC) AS row_num
    FROM validated_feature_usage
    WHERE USAGE_COUNT >= 0
)

SELECT 
    USAGE_ID,
    MEETING_ID,
    FEATURE_NAME,
    USAGE_COUNT,
    USAGE_DATE,
    LOAD_TIMESTAMP,
    UPDATE_TIMESTAMP,
    SOURCE_SYSTEM,
    LOAD_DATE,
    UPDATE_DATE,
    DATA_QUALITY_SCORE,
    VALIDATION_STATUS,
    CREATED_AT,
    UPDATED_AT
FROM deduped_feature_usage
WHERE row_num = 1
