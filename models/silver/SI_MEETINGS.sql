-- Silver Layer Meetings Table
-- Transforms and cleanses meeting data from Bronze layer with duration text cleaning

WITH bronze_meetings AS (
    SELECT 
        MEETING_ID,
        HOST_ID,
        MEETING_TOPIC,
        START_TIME,
        END_TIME,
        DURATION_MINUTES,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM,
        ROW_NUMBER() OVER (PARTITION BY MEETING_ID ORDER BY UPDATE_TIMESTAMP DESC) AS rn
    FROM {{ source('bronze', 'BZ_MEETINGS') }}
    WHERE MEETING_ID IS NOT NULL
),

cleaned_meetings AS (
    SELECT 
        *,
        -- Clean duration minutes - remove text units like 'mins', 'minutes'
        CASE 
            WHEN DURATION_MINUTES IS NULL THEN NULL
            WHEN DURATION_MINUTES::STRING REGEXP '[a-zA-Z]' THEN 
                TRY_TO_NUMBER(REGEXP_REPLACE(DURATION_MINUTES::STRING, '[^0-9.]', ''))
            ELSE DURATION_MINUTES
        END AS CLEAN_DURATION_MINUTES,
        
        -- Handle EST timezone format in timestamps
        CASE 
            WHEN START_TIME::STRING LIKE '%EST%' THEN 
                TRY_TO_TIMESTAMP(REPLACE(START_TIME::STRING, ' EST', ''), 'YYYY-MM-DD HH24:MI:SS')
            ELSE START_TIME
        END AS CLEAN_START_TIME,
        
        CASE 
            WHEN END_TIME::STRING LIKE '%EST%' THEN 
                TRY_TO_TIMESTAMP(REPLACE(END_TIME::STRING, ' EST', ''), 'YYYY-MM-DD HH24:MI:SS')
            ELSE END_TIME
        END AS CLEAN_END_TIME
    FROM bronze_meetings
    WHERE rn = 1
),

data_quality_checks AS (
    SELECT 
        *,
        -- Calculate actual duration for validation
        CASE 
            WHEN CLEAN_START_TIME IS NOT NULL AND CLEAN_END_TIME IS NOT NULL THEN
                DATEDIFF('minute', CLEAN_START_TIME, CLEAN_END_TIME)
            ELSE NULL
        END AS CALCULATED_DURATION,
        
        -- Data quality scoring
        CASE 
            WHEN MEETING_ID IS NULL THEN 0
            WHEN HOST_ID IS NULL THEN 20
            WHEN CLEAN_START_TIME IS NULL OR CLEAN_END_TIME IS NULL THEN 30
            WHEN CLEAN_END_TIME <= CLEAN_START_TIME THEN 40
            WHEN CLEAN_DURATION_MINUTES IS NULL OR CLEAN_DURATION_MINUTES < 0 OR CLEAN_DURATION_MINUTES > 1440 THEN 50
            WHEN ABS(COALESCE(CLEAN_DURATION_MINUTES, 0) - COALESCE(DATEDIFF('minute', CLEAN_START_TIME, CLEAN_END_TIME), 0)) > 5 THEN 70
            ELSE 100
        END AS DATA_QUALITY_SCORE,
        
        CASE 
            WHEN MEETING_ID IS NULL OR HOST_ID IS NULL THEN 'FAILED'
            WHEN CLEAN_START_TIME IS NULL OR CLEAN_END_TIME IS NULL THEN 'FAILED'
            WHEN CLEAN_END_TIME <= CLEAN_START_TIME THEN 'FAILED'
            WHEN CLEAN_DURATION_MINUTES IS NULL OR CLEAN_DURATION_MINUTES < 0 OR CLEAN_DURATION_MINUTES > 1440 THEN 'WARNING'
            ELSE 'PASSED'
        END AS VALIDATION_STATUS
    FROM cleaned_meetings
)

SELECT 
    MEETING_ID,
    HOST_ID,
    TRIM(MEETING_TOPIC) AS MEETING_TOPIC,
    CLEAN_START_TIME AS START_TIME,
    CLEAN_END_TIME AS END_TIME,
    COALESCE(CLEAN_DURATION_MINUTES, CALCULATED_DURATION, 0) AS DURATION_MINUTES,
    LOAD_TIMESTAMP,
    UPDATE_TIMESTAMP,
    SOURCE_SYSTEM,
    CURRENT_DATE() AS LOAD_DATE,
    CURRENT_DATE() AS UPDATE_DATE,
    DATA_QUALITY_SCORE,
    VALIDATION_STATUS
FROM data_quality_checks
WHERE VALIDATION_STATUS IN ('PASSED', 'WARNING')
