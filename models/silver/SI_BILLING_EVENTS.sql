/*
 * SI_BILLING_EVENTS - Silver Layer Billing Events Table
 */

WITH bronze_billing_events AS (
    SELECT 
        EVENT_ID,
        USER_ID,
        EVENT_TYPE,
        AMOUNT,
        EVENT_DATE,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM
    FROM BRONZE.BZ_BILLING_EVENTS
    WHERE EVENT_ID IS NOT NULL
      AND COALESCE(AMOUNT, 0) > 0
      AND COALESCE(EVENT_DATE, CURRENT_DATE()) <= CURRENT_DATE()
)

SELECT 
    EVENT_ID,
    USER_ID,
    UPPER(TRIM(EVENT_TYPE)) AS EVENT_TYPE,
    ROUND(AMOUNT, 2) AS AMOUNT,
    EVENT_DATE,
    LOAD_TIMESTAMP,
    UPDATE_TIMESTAMP,
    SOURCE_SYSTEM,
    DATE(LOAD_TIMESTAMP) AS LOAD_DATE,
    COALESCE(DATE(UPDATE_TIMESTAMP), DATE(LOAD_TIMESTAMP)) AS UPDATE_DATE,
    
    /* Data Quality Score Calculation */
    CASE 
        WHEN EVENT_ID IS NOT NULL 
            AND USER_ID IS NOT NULL 
            AND EVENT_TYPE IS NOT NULL 
            AND AMOUNT IS NOT NULL 
            AND AMOUNT > 0
            AND EVENT_DATE IS NOT NULL
            AND EVENT_DATE <= CURRENT_DATE()
        THEN 100
        WHEN EVENT_ID IS NOT NULL AND USER_ID IS NOT NULL AND EVENT_TYPE IS NOT NULL 
        THEN 75
        WHEN EVENT_ID IS NOT NULL 
        THEN 50
        ELSE 25
    END AS DATA_QUALITY_SCORE,
    
    /* Validation Status */
    CASE 
        WHEN EVENT_ID IS NOT NULL 
            AND USER_ID IS NOT NULL 
            AND EVENT_TYPE IS NOT NULL 
            AND AMOUNT IS NOT NULL 
            AND AMOUNT > 0
            AND EVENT_DATE IS NOT NULL
            AND EVENT_DATE <= CURRENT_DATE()
        THEN 'PASSED'
        WHEN EVENT_ID IS NOT NULL AND USER_ID IS NOT NULL 
        THEN 'WARNING'
        ELSE 'FAILED'
    END AS VALIDATION_STATUS
FROM bronze_billing_events
