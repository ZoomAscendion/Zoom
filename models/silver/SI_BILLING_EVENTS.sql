{{ config(
    materialized='table',
    unique_key='EVENT_ID'
) }}

WITH bronze_billing_events AS (
    SELECT 
        EVENT_ID,
        USER_ID,
        EVENT_TYPE,
        AMOUNT,
        EVENT_DATE,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM
    FROM {{ source('bronze', 'BZ_BILLING_EVENTS') }}
    WHERE EVENT_ID IS NOT NULL
),

cleaned_billing_events AS (
    SELECT 
        EVENT_ID,
        USER_ID,
        UPPER(TRIM(EVENT_TYPE)) AS EVENT_TYPE,
        CASE 
            WHEN TRY_TO_NUMBER(REPLACE(AMOUNT::STRING, '"', '')) IS NOT NULL THEN
                TRY_TO_NUMBER(REPLACE(AMOUNT::STRING, '"', ''))
            ELSE TRY_TO_NUMBER(AMOUNT::STRING)
        END AS CLEANED_AMOUNT,
        COALESCE(
            TRY_TO_DATE(EVENT_DATE::STRING, 'YYYY-MM-DD'),
            TRY_TO_DATE(EVENT_DATE::STRING, 'DD/MM/YYYY'),
            TRY_TO_DATE(EVENT_DATE::STRING, 'MM/DD/YYYY'),
            TRY_TO_DATE(EVENT_DATE::STRING)
        ) AS CLEANED_EVENT_DATE,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM
    FROM bronze_billing_events
),

validated_billing_events AS (
    SELECT 
        EVENT_ID,
        USER_ID,
        EVENT_TYPE,
        ROUND(COALESCE(CLEANED_AMOUNT, 0), 2) AS AMOUNT,
        COALESCE(CLEANED_EVENT_DATE, CURRENT_DATE()) AS EVENT_DATE,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM,
        DATE(LOAD_TIMESTAMP) AS LOAD_DATE,
        DATE(UPDATE_TIMESTAMP) AS UPDATE_DATE,
        CASE 
            WHEN USER_ID IS NOT NULL AND EVENT_TYPE IS NOT NULL AND CLEANED_AMOUNT > 0 AND CLEANED_EVENT_DATE IS NOT NULL THEN 100
            WHEN USER_ID IS NOT NULL AND EVENT_TYPE IS NOT NULL AND CLEANED_AMOUNT >= 0 THEN 85
            WHEN USER_ID IS NOT NULL AND EVENT_TYPE IS NOT NULL THEN 70
            ELSE 50
        END AS DATA_QUALITY_SCORE,
        CASE 
            WHEN USER_ID IS NOT NULL AND EVENT_TYPE IS NOT NULL AND CLEANED_AMOUNT > 0 AND CLEANED_EVENT_DATE IS NOT NULL THEN 'PASSED'
            WHEN USER_ID IS NOT NULL AND EVENT_TYPE IS NOT NULL AND CLEANED_AMOUNT >= 0 THEN 'WARNING'
            ELSE 'FAILED'
        END AS VALIDATION_STATUS
    FROM cleaned_billing_events
),

deduped_billing_events AS (
    SELECT 
        EVENT_ID,
        USER_ID,
        EVENT_TYPE,
        AMOUNT,
        EVENT_DATE,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM,
        LOAD_DATE,
        UPDATE_DATE,
        DATA_QUALITY_SCORE,
        VALIDATION_STATUS,
        CURRENT_TIMESTAMP() AS CREATED_AT,
        CURRENT_TIMESTAMP() AS UPDATED_AT,
        ROW_NUMBER() OVER (PARTITION BY EVENT_ID ORDER BY UPDATE_TIMESTAMP DESC) AS row_num
    FROM validated_billing_events
    WHERE AMOUNT >= 0 AND EVENT_DATE <= CURRENT_DATE()
)

SELECT 
    EVENT_ID,
    USER_ID,
    EVENT_TYPE,
    AMOUNT,
    EVENT_DATE,
    LOAD_TIMESTAMP,
    UPDATE_TIMESTAMP,
    SOURCE_SYSTEM,
    LOAD_DATE,
    UPDATE_DATE,
    DATA_QUALITY_SCORE,
    VALIDATION_STATUS,
    CREATED_AT,
    UPDATED_AT
FROM deduped_billing_events
WHERE row_num = 1
