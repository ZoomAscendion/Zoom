/* Silver Layer Licenses Table Transformation */
/* Includes critical P1 fix for DD/MM/YYYY date format conversion */

WITH bronze_licenses AS (
    SELECT 
        LICENSE_ID,
        LICENSE_TYPE,
        ASSIGNED_TO_USER_ID,
        START_DATE,
        END_DATE,
        LOAD_TIMESTAMP,
        UPDATE_TIMESTAMP,
        SOURCE_SYSTEM
    FROM {{ source('bronze', 'BZ_LICENSES') }}
    WHERE LICENSE_ID IS NOT NULL
),

date_cleaning AS (
    SELECT 
        *,
        /* Critical P1 Fix: DD/MM/YYYY date format conversion for START_DATE */
        COALESCE(
            TRY_TO_DATE(START_DATE, 'YYYY-MM-DD'),
            TRY_TO_DATE(START_DATE, 'DD/MM/YYYY'),
            TRY_TO_DATE(START_DATE, 'MM/DD/YYYY'),
            TRY_TO_DATE(START_DATE)
        ) AS CLEAN_START_DATE,
        
        /* Critical P1 Fix: DD/MM/YYYY date format conversion for END_DATE */
        COALESCE(
            TRY_TO_DATE(END_DATE, 'YYYY-MM-DD'),
            TRY_TO_DATE(END_DATE, 'DD/MM/YYYY'),
            TRY_TO_DATE(END_DATE, 'MM/DD/YYYY'),
            TRY_TO_DATE(END_DATE)
        ) AS CLEAN_END_DATE
    FROM bronze_licenses
),

data_quality_checks AS (
    SELECT 
        *,
        /* Data Quality Score Calculation */
        (
            CASE WHEN LICENSE_ID IS NOT NULL THEN 20 ELSE 0 END +
            CASE WHEN LICENSE_TYPE IS NOT NULL AND LENGTH(TRIM(LICENSE_TYPE)) > 0 THEN 15 ELSE 0 END +
            CASE WHEN ASSIGNED_TO_USER_ID IS NOT NULL THEN 20 ELSE 0 END +
            CASE WHEN CLEAN_START_DATE IS NOT NULL THEN 20 ELSE 0 END +
            CASE WHEN CLEAN_END_DATE IS NOT NULL THEN 20 ELSE 0 END +
            CASE WHEN CLEAN_START_DATE IS NOT NULL AND CLEAN_END_DATE IS NOT NULL AND CLEAN_START_DATE <= CLEAN_END_DATE THEN 5 ELSE 0 END
        ) AS DATA_QUALITY_SCORE,
        
        /* Validation Status */
        CASE 
            WHEN LICENSE_ID IS NULL OR ASSIGNED_TO_USER_ID IS NULL THEN 'FAILED'
            WHEN CLEAN_START_DATE IS NULL OR CLEAN_END_DATE IS NULL THEN 'FAILED'
            WHEN CLEAN_START_DATE > CLEAN_END_DATE THEN 'FAILED'
            ELSE 'PASSED'
        END AS VALIDATION_STATUS
    FROM date_cleaning
),

deduplication AS (
    SELECT *,
        ROW_NUMBER() OVER (PARTITION BY LICENSE_ID ORDER BY UPDATE_TIMESTAMP DESC NULLS LAST, LOAD_TIMESTAMP DESC) as rn
    FROM data_quality_checks
)

SELECT 
    LICENSE_ID,
    UPPER(TRIM(LICENSE_TYPE)) AS LICENSE_TYPE,
    ASSIGNED_TO_USER_ID,
    CLEAN_START_DATE AS START_DATE,
    CLEAN_END_DATE AS END_DATE,
    LOAD_TIMESTAMP,
    UPDATE_TIMESTAMP,
    SOURCE_SYSTEM,
    DATE(LOAD_TIMESTAMP) AS LOAD_DATE,
    DATE(UPDATE_TIMESTAMP) AS UPDATE_DATE,
    DATA_QUALITY_SCORE,
    VALIDATION_STATUS
FROM deduplication
WHERE rn = 1
  AND VALIDATION_STATUS != 'FAILED'
  AND CLEAN_START_DATE IS NOT NULL
  AND CLEAN_END_DATE IS NOT NULL
