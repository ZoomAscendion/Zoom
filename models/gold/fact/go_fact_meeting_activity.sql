-- Meeting activity fact table transformation from Silver to Gold layer
WITH meeting_base AS (
    SELECT 
        m.MEETING_ID,
        m.HOST_ID,
        m.MEETING_TOPIC,
        m.START_TIME,
        m.END_TIME,
        m.DURATION_MINUTES,
        m.SOURCE_SYSTEM,
        DATE(m.START_TIME) AS MEETING_DATE
    FROM {{ source('silver', 'si_meetings') }} m
    WHERE m.VALIDATION_STATUS = 'PASSED'
),

participant_metrics AS (
    SELECT 
        p.MEETING_ID,
        COUNT(DISTINCT p.USER_ID) AS PARTICIPANT_COUNT,
        SUM(DATEDIFF('minute', p.JOIN_TIME, p.LEAVE_TIME)) AS TOTAL_PARTICIPANT_MINUTES,
        AVG(DATEDIFF('minute', p.JOIN_TIME, p.LEAVE_TIME)) AS AVERAGE_PARTICIPATION_MINUTES,
        COUNT(CASE WHEN p.JOIN_TIME > DATEADD('minute', 5, 
            (SELECT START_TIME FROM meeting_base mb WHERE mb.MEETING_ID = p.MEETING_ID)
        ) THEN 1 END) AS LATE_JOINERS_COUNT,
        COUNT(CASE WHEN p.LEAVE_TIME < DATEADD('minute', -5, 
            (SELECT END_TIME FROM meeting_base mb WHERE mb.MEETING_ID = p.MEETING_ID)
        ) THEN 1 END) AS EARLY_LEAVERS_COUNT
    FROM {{ source('silver', 'si_participants') }} p
    WHERE p.VALIDATION_STATUS = 'PASSED'
    GROUP BY p.MEETING_ID
),

feature_metrics AS (
    SELECT 
        f.MEETING_ID,
        COUNT(DISTINCT f.FEATURE_NAME) AS FEATURES_USED_COUNT,
        SUM(CASE WHEN UPPER(f.FEATURE_NAME) LIKE '%SCREEN%SHARE%' THEN f.USAGE_COUNT ELSE 0 END) AS SCREEN_SHARE_DURATION_MINUTES,
        SUM(CASE WHEN UPPER(f.FEATURE_NAME) LIKE '%RECORD%' THEN f.USAGE_COUNT ELSE 0 END) AS RECORDING_DURATION_MINUTES,
        SUM(CASE WHEN UPPER(f.FEATURE_NAME) LIKE '%CHAT%' THEN f.USAGE_COUNT ELSE 0 END) AS CHAT_MESSAGES_COUNT,
        SUM(CASE WHEN UPPER(f.FEATURE_NAME) LIKE '%FILE%' THEN f.USAGE_COUNT ELSE 0 END) AS FILE_SHARES_COUNT,
        SUM(CASE WHEN UPPER(f.FEATURE_NAME) LIKE '%BREAKOUT%' THEN f.USAGE_COUNT ELSE 0 END) AS BREAKOUT_ROOMS_USED,
        SUM(CASE WHEN UPPER(f.FEATURE_NAME) LIKE '%POLL%' THEN f.USAGE_COUNT ELSE 0 END) AS POLLS_CONDUCTED
    FROM {{ source('silver', 'si_feature_usage') }} f
    WHERE f.VALIDATION_STATUS = 'PASSED'
    GROUP BY f.MEETING_ID
),

meeting_activity_fact AS (
    SELECT 
        ROW_NUMBER() OVER (ORDER BY mb.MEETING_ID) AS MEETING_ACTIVITY_ID,
        dd.DATE_ID AS DATE_ID,
        dmt.MEETING_TYPE_ID AS MEETING_TYPE_ID,
        du.USER_DIM_ID AS HOST_USER_DIM_ID,
        mb.MEETING_ID,
        mb.MEETING_DATE,
        mb.START_TIME AS MEETING_START_TIME,
        mb.END_TIME AS MEETING_END_TIME,
        mb.DURATION_MINUTES AS SCHEDULED_DURATION_MINUTES,
        mb.DURATION_MINUTES AS ACTUAL_DURATION_MINUTES,
        COALESCE(pm.PARTICIPANT_COUNT, 0) AS PARTICIPANT_COUNT,
        COALESCE(pm.PARTICIPANT_COUNT, 0) AS UNIQUE_PARTICIPANTS,
        mb.DURATION_MINUTES AS HOST_DURATION_MINUTES,
        COALESCE(pm.TOTAL_PARTICIPANT_MINUTES, 0) AS TOTAL_PARTICIPANT_MINUTES,
        COALESCE(pm.AVERAGE_PARTICIPATION_MINUTES, 0) AS AVERAGE_PARTICIPATION_MINUTES,
        COALESCE(pm.PARTICIPANT_COUNT, 0) AS PEAK_CONCURRENT_PARTICIPANTS,
        COALESCE(pm.LATE_JOINERS_COUNT, 0) AS LATE_JOINERS_COUNT,
        COALESCE(pm.EARLY_LEAVERS_COUNT, 0) AS EARLY_LEAVERS_COUNT,
        COALESCE(fm.FEATURES_USED_COUNT, 0) AS FEATURES_USED_COUNT,
        COALESCE(fm.SCREEN_SHARE_DURATION_MINUTES, 0) AS SCREEN_SHARE_DURATION_MINUTES,
        COALESCE(fm.RECORDING_DURATION_MINUTES, 0) AS RECORDING_DURATION_MINUTES,
        COALESCE(fm.CHAT_MESSAGES_COUNT, 0) AS CHAT_MESSAGES_COUNT,
        COALESCE(fm.FILE_SHARES_COUNT, 0) AS FILE_SHARES_COUNT,
        COALESCE(fm.BREAKOUT_ROOMS_USED, 0) AS BREAKOUT_ROOMS_USED,
        COALESCE(fm.POLLS_CONDUCTED, 0) AS POLLS_CONDUCTED,
        CASE 
            WHEN COALESCE(pm.PARTICIPANT_COUNT, 0) >= 5 AND COALESCE(pm.AVERAGE_PARTICIPATION_MINUTES, 0) >= (mb.DURATION_MINUTES * 0.8) THEN 5.0
            WHEN COALESCE(pm.PARTICIPANT_COUNT, 0) >= 3 AND COALESCE(pm.AVERAGE_PARTICIPATION_MINUTES, 0) >= (mb.DURATION_MINUTES * 0.6) THEN 4.0
            WHEN COALESCE(pm.PARTICIPANT_COUNT, 0) >= 2 AND COALESCE(pm.AVERAGE_PARTICIPATION_MINUTES, 0) >= (mb.DURATION_MINUTES * 0.4) THEN 3.0
            WHEN COALESCE(pm.PARTICIPANT_COUNT, 0) >= 1 AND COALESCE(pm.AVERAGE_PARTICIPATION_MINUTES, 0) >= (mb.DURATION_MINUTES * 0.2) THEN 2.0
            ELSE 1.0
        END AS MEETING_QUALITY_SCORE,
        4.5 AS AUDIO_QUALITY_SCORE,
        4.5 AS VIDEO_QUALITY_SCORE,
        0 AS CONNECTION_ISSUES_COUNT,
        4.0 AS MEETING_SATISFACTION_SCORE,
        CURRENT_DATE() AS LOAD_DATE,
        CURRENT_DATE() AS UPDATE_DATE,
        mb.SOURCE_SYSTEM
    FROM meeting_base mb
    LEFT JOIN {{ ref('go_dim_date') }} dd ON mb.MEETING_DATE = dd.DATE_VALUE
    LEFT JOIN {{ ref('go_dim_meeting_type') }} dmt ON 1=1 -- Simplified join for demo
    LEFT JOIN {{ ref('go_dim_user') }} du ON mb.HOST_ID = du.USER_ID AND du.IS_CURRENT_RECORD = TRUE
    LEFT JOIN participant_metrics pm ON mb.MEETING_ID = pm.MEETING_ID
    LEFT JOIN feature_metrics fm ON mb.MEETING_ID = fm.MEETING_ID
)

SELECT * FROM meeting_activity_fact
