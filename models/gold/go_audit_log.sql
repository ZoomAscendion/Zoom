-- Gold Layer Process Audit Log
-- This table must be created first and run before any other models

SELECT 
    {{ dbt_utils.generate_surrogate_key(['PROCESS_NAME', 'EXECUTION_START_TIMESTAMP']) }} AS AUDIT_LOG_ID,
    'INITIAL_SETUP' AS PROCESS_NAME,
    'GOLD_LAYER_ETL' AS PROCESS_TYPE,
    CURRENT_TIMESTAMP() AS EXECUTION_START_TIMESTAMP,
    NULL AS EXECUTION_END_TIMESTAMP,
    NULL AS EXECUTION_DURATION_SECONDS,
    'RUNNING' AS EXECUTION_STATUS,
    'SYSTEM' AS SOURCE_TABLE_NAME,
    'GO_PROCESS_AUDIT_LOG' AS TARGET_TABLE_NAME,
    0 AS RECORDS_READ,
    0 AS RECORDS_PROCESSED,
    0 AS RECORDS_INSERTED,
    0 AS RECORDS_UPDATED,
    0 AS RECORDS_FAILED,
    100.0 AS DATA_QUALITY_SCORE,
    0 AS ERROR_COUNT,
    0 AS WARNING_COUNT,
    'INITIAL_SETUP' AS PROCESS_TRIGGER,
    'DBT_SYSTEM' AS EXECUTED_BY,
    'SNOWFLAKE' AS SERVER_NAME,
    '1.0.0' AS PROCESS_VERSION,
    PARSE_JSON('{}') AS CONFIGURATION_PARAMETERS,
    PARSE_JSON('{}') AS PERFORMANCE_METRICS,
    CURRENT_DATE() AS LOAD_DATE,
    CURRENT_DATE() AS UPDATE_DATE,
    'DBT_GOLD_PIPELINE' AS SOURCE_SYSTEM
WHERE FALSE -- This ensures no actual data is inserted during initial setup

UNION ALL

-- Create the table structure
SELECT 
    'AUDIT_SETUP' AS AUDIT_LOG_ID,
    'AUDIT_LOG_INITIALIZATION' AS PROCESS_NAME,
    'SETUP' AS PROCESS_TYPE,
    CURRENT_TIMESTAMP() AS EXECUTION_START_TIMESTAMP,
    CURRENT_TIMESTAMP() AS EXECUTION_END_TIMESTAMP,
    0.0 AS EXECUTION_DURATION_SECONDS,
    'SUCCESS' AS EXECUTION_STATUS,
    'SYSTEM' AS SOURCE_TABLE_NAME,
    'GO_PROCESS_AUDIT_LOG' AS TARGET_TABLE_NAME,
    1 AS RECORDS_READ,
    1 AS RECORDS_PROCESSED,
    1 AS RECORDS_INSERTED,
    0 AS RECORDS_UPDATED,
    0 AS RECORDS_FAILED,
    100.0 AS DATA_QUALITY_SCORE,
    0 AS ERROR_COUNT,
    0 AS WARNING_COUNT,
    'SYSTEM_INITIALIZATION' AS PROCESS_TRIGGER,
    'DBT_SYSTEM' AS EXECUTED_BY,
    'SNOWFLAKE' AS SERVER_NAME,
    '1.0.0' AS PROCESS_VERSION,
    PARSE_JSON('{"initialization": true}') AS CONFIGURATION_PARAMETERS,
    PARSE_JSON('{"setup_time_seconds": 1}') AS PERFORMANCE_METRICS,
    CURRENT_DATE() AS LOAD_DATE,
    CURRENT_DATE() AS UPDATE_DATE,
    'DBT_GOLD_PIPELINE' AS SOURCE_SYSTEM
